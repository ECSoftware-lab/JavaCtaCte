    private void btnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarActionPerformed
        // TODO add your handling code here:
        
        if(TTotal.getText().length()==0){
            this.mensaje("debes ingresar un total");
            TTotal.requestFocus();
            return ;
        }
        
        int aux =0;
        try {
            Integer.parseInt(TNProveedor.getText());
        } catch (Exception e) {
            this.mensaje("debes tener un numero de proveedor");
            return ;
        }
        
        
        
        CPagos dts = new CPagos();
        
        LPagos func = new LPagos();
        //mensaje("por ingresar 1");
        dts.setIdProveedor(Integer.parseInt(TNProveedor.getText()));
        dts.setIdresponsable(10);
        dts.setTotal(Double.parseDouble(TTotal.getText()));
        dts.setTipo(CTipo.getSelectedItem().toString());
        dts.setUnico("--");
        dts.setDescripcion(TDescripcion.getText());
        dts.setNTransaccion(TNComprobante.getText());
        
        
            try {
        //jDateChooser el nombre la variable  del componente jdatecgooser
         Date  fecha=TFecha.getDate();
        DateFormat f=new SimpleDateFormat("yyyy-MM-dd");
        String fecha2=f.format(fecha); 
        //textFecha nombre de la variable del componenten jtextfiel
        //TNComprobante.setText(fecha2);
        //mensaje(fecha2);
        
        dts.setFecha(fecha2);
       
    } catch (NullPointerException  e) {
                mensaje("Fecha Incorrecta ");
                return;
    }
        if(accion.equals("guardar")){
            if(func.insertar(dts)){
                mensaje("Se guardo el pago");
                mostrar(TNProveedor.getText());
                limpiarcampos();
                LAccion.setText("Formulario en modo: ");
                accion="";
            }
        }else{
            if(accion.equals("modificar")){
                mensaje("NO Tiene Permiso \n para modificar");
            }else{
                mensaje("NO Selecciono opcion");
            }
        }
        
        
       
    }//GEN-LAST:event_btnConfirmarActionPerformed

