    void inhabilitar(){
        TNProveedor.setEnabled(false);
        CTipo.setEnabled(false);
        TTotal.setEditable(false);
        TNComprobante.setEnabled(false);
    }
    void limpiarcampos(){
        TNProveedor.setText("");
        CTipo.setSelectedIndex(0);
        TTotal.setText("");
        TNComprobante.setText("");
        TDescripcion.setText("");
        
    }
      void habilitar(){
        TNProveedor.setEnabled(true);
        CTipo.setEnabled(true);
        TTotal.setEditable(true);
        TNComprobante.setEnabled(true);
    }
      
    void mostrar (String buscar){
        try {
            DefaultTableModel modelo;
            LPagos func= new LPagos();
            modelo = func.mostrar(buscar);
          
            TPagosHisto.setModel(modelo);
            //ocultar_colummas();
            LTotalRegistros.setText("Total Reg "+Integer.toString(func.totalregistros));
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null,e);
        }
    }
    
