    private void TProveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TProveedoresMouseClicked
        // TODO add your handling code here:
        int seleccionar = TProveedores.rowAtPoint(evt.getPoint());
        TxNProveedor.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 0)));
        TxRazonSocial.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 1)));
        TxCuit.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 2)));
       // CoProvincia.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 3)));
       //CoLocalidad.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 4)));
       TxDomicilio.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 5)));
       //TxTelefono.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 5)));
       TxTelefono.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 6)));
       TxCelular.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 7)));
       TxEmail.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 8)));
       String Baja= String.valueOf(TProveedores.getValueAt(seleccionar, 12));
       
       if(Baja.equals("SI"))
       { ChInactivo.setSelected(true);
        //mensaje("SIII");     
       }
       else
       {    
          ChInactivo.setSelected(false);
        //mensaje("nooooooo");
       }
       
       TxEmail.setText(Baja);
       TxPiso.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 13)));
        TxDepto.setText(String.valueOf(TProveedores.getValueAt(seleccionar, 14)));
        
    }//GEN-LAST:event_TProveedoresMouseClicked

